          |.......
          |......."""),
      TestFrame(5, List(Left),
        """.T.....
          |TTT....
          |.......
          |......."""),
      TestFrame(5, List(Down),
        """.......
          |.T.....
          |TTT....
          |......."""),
      TestFrame(5, List(Right),
        """.......
          |..T....
          |.TTT...
          |......."""),
      TestFrame(5, List(Right),
        """.......
          |...T...
          |..TTT..
          |......."""),
      TestFrame(5, List(Right),
        """.......
          |....T..
          |...TTT.
          |......."""),
      TestFrame(5, List(Right),
        """.......
          |.....T.
          |....TTT
          |......."""),
      TestFrame(5, List(Down),
        """.......
          |.......
          |.....T.
          |....TTT"""),
      TestFrame(5, List(Left),
        """.......
          |.......
          |....T..
          |...TTT."""),
      TestFrame(5, List(Left),
        """.......
          |.......
          |...T...
          |..TTT.."""),
      TestFrame(5, List(Left),
        """.......
          |.......
          |..T....
          |.TTT..."""),
      TestFrame(5, List(Left),
        """.......
          |.......
          |.T.....
          |TTT...."""))
      ,hint = "Moving left, right and down should work as expected")
  }

}
