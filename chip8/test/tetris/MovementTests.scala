package tetris

class MovementTests extends TetrisTestSuiteBase {

  test("testMovement") {
    checkGame( List(TestFrame(4,
      """..SS..
        |.SS...
        |......
        |......"""),
      TestFrame(4, List(Left),
        """.SS...
          |SS....
          |......
          |......"""),
      TestFrame(4, List(Right),
        """..SS..
          |.SS...
          |......
          |......"""),
      TestFrame(4, List(Right),
        """...SS.
          |..SS..
          |......
          |......"""),
      TestFrame(4, List(Right),
        """....SS
          |...SS.
          |......
          |......"""),
      TestFrame(4, List(Down),
        """......
          |....SS
          |...SS.
          |......"""),
      TestFrame(4, List(Left),
        """......
          |...SS.
          |..SS..
          |......"""),
      TestFrame(4, List(Left),
        """......
          |..SS..
          |.SS...
          |......"""),
      TestFrame(4, List(Left),
        """......
          |.SS...
          |SS....
          |......"""),
      TestFrame(4, List(Down),
        """......
          |......
          |.SS...
          |SS...."""),
      TestFrame(4, List(Right),
        """......
          |......
          |..SS..
          |.SS..."""),
      TestFrame(4, List(Right),
        """......
          |......
          |...SS.
          |..SS.."""),
      TestFrame(4, List(Right),
        """......
          |......
          |....SS
          |...SS."""))
    ,hint = "Moving left, right and down should work as expected")
  }

  test("testMovement2") {
